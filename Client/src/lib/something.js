const spaceImageUrl = "https://lingojam.com/img/ASL_signs/_space.png";

// Function to split the array at space URLs
export const splitArrayAtSpace = (array) => {
  const result = [];
  let currentSubarray = [];

  for (const imageUrl of array) {
    if (imageUrl === spaceImageUrl) {
      // Save the current subarray and start a new one
      if (currentSubarray.length > 0) {
        result.push(currentSubarray);
        currentSubarray = [];
      }
    } else {
      // Add the image URL to the current subarray
      currentSubarray.push(imageUrl);
    }
  }

  // Add the last subarray if not empty
  if (currentSubarray.length > 0) {
    result.push(currentSubarray);
  }
  return result;
};
